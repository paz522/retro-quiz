// 選択肢をシャッフルする関数
const shuffleOptions = (quiz: Quiz): Quiz => {
  const correctAnswer = quiz.correctAnswer;
  const options = [...quiz.options];
  
  // Fisher-Yatesアルゴリズムで選択肢をシャッフル
  for (let i = options.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [options[i], options[j]] = [options[j], options[i]];
  }
  
  return {
    ...quiz,
    options,
    correctAnswer
  };
};

export const getQuizzesByCategory = (category: CategoryId) => {
  return quizzes.filter(quiz => quiz.category === category).map(shuffleOptions);
};

export const getQuizzesByEra = (era: EraId) => {
  return quizzes.filter(quiz => quiz.era === era).map(shuffleOptions);
};

export const getRandomQuizzes = (count: number) => {
  const shuffled = [...quizzes].sort(() => 0.5 - Math.random());
  
  // 各クイズの選択肢をシャッフル
  return shuffled.slice(0, count).map(shuffleOptions);
}; 